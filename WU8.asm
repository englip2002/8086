.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB ?
	NUM2 DB ?
	STR1 DB "ENTER 1ST DIGIT: $"
	STR2 DB "ENTER 2ND DIGIT: $"
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;--OUTPUT STR1
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;--INPUT NUM1
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM1,AL
	
	;--OUTPUT NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--OUTPUT STR2
	MOV AH,09H
	LEA DX,STR2
	INT 21H
	
	;--INPUT NUM2
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM2,AL
	
	;--OUTPUT NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV BL,NUM1
	CMP BL,NUM2
	JGE PNUM1
	
	MOV AH,02H
	MOV DL,NUM2
	ADD DL,30H
	INT 21H
	JMP EXIT
	
PNUM1:
	MOV AH,02H
	MOV DL,NUM1
	ADD DL,30H
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN