.MODEL SMALL
.STACK 100
.DATA
		STR1 DB 'Enter 1st digit: $'
		STR2 DB 'Enter 2nd digit: $'
		STR3 DB 'Product is $'
		STR4 DB 'Sum is $'
		STR5 DB 'Different is $'
		NL DB 10,13,'$'
		NUM1 DB ?
		NUM2 DB ?
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;--OUTPUT STR1
		MOV AH,09H
		LEA DX,STR1
		INT 21H
		
		;--INPUT NUM1
		MOV AH,01H
		INT 21H
		SUB AL,30H
		MOV NUM1,AL
		
		;--OUTPUT NL
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		;--OUTPUT STR1
		MOV AH,09H
		LEA DX,STR2
		INT 21H
		
		;--INPUT NUM2
		MOV AH,01H
		INT 21H
		SUB AL,30H
		MOV NUM2,AL
		
		;--OUTPUT NL
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		;--OUTPUT STR3
		MOV AH,09H
		LEA DX,STR3
		INT 21H
		
		;--CAL RESULT,MUL
		MOV AL,NUM1
		MUL NUM2
		MOV BX, AX
		
		;--OUTPUT RESULT, BL
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		;--OUTPUT NL
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		;--OUTPUT STR4
		MOV AH,09H
		LEA DX,STR4
		INT 21H
		
		;--CAL ADD
		MOV BL,NUM1
		ADD BL,NUM2
		
		;--OUTPUT RESULT
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		;--OUTPUT NL
		MOV AH,09H
		LEA DX,NL
		INT 21H
		
		;--OUTPUT STR5
		MOV AH,09H
		LEA DX,STR5
		INT 21H
		
		;--CAL DIFF
		MOV BL,NUM1
		SUB BL,NUM2
		
		;--OUTPUT RESULT
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		MOV AX,4c00H
		INT 21H
MAIN ENDP
END MAIN