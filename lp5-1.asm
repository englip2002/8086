.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB 17
	NUM2 DB 21
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CL,NUM2
	SUB CL,NUM1
	ADD CL,1
	MOV CH,0
	
	;LOOP
	L1:
		MOV AH,0
		MOV AL,NUM1
		DIV TEN
		MOV BX,AX
		
		;OUTPUT QUOTIENT
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		;OUTPUT REMAINDER
		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H
		
		;OUTPUT SPACE
		MOV AH,02H
		MOV DL," "
		INT 21H
		INC NUM1
	LOOP L1
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN