.MODEL SMALL
.STACK 100
.DATA
	IMSG DB "ENTER A DIGIT: $"
	IDIGIT DB ?
	NL DB 10,13,"$"
	NUM DB 9,4,5,8,7
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV IDIGIT,AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV CH,0
	MOV CL,IDIGIT
	
	MOV SI,0
	L1:
		MOV AH,02H
		MOV DL,NUM[SI]
		ADD DL,30H
		INT 21H
		INC SI
	LOOP L1
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN