.MODEL SMALL
.STACK 100
.DATA
		NUM1 DB 3
		NUM2 DB 5
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;--SHOW 3
		MOV AH,02H
		MOV DL, NUM1
		ADD DL,30H
		INT 21H
		
		;--SHOW + | ASCII=43
		MOV AH,02H
		MOV DL,43
		INT 21H
		
		;--SHOW 5
		MOV AH,02H
		MOV DL,NUM2
		ADD DL,30H
		INT 21H
		
		;--SHOW =  | ASCII=61
		MOV AH,02H
		MOV DL,61
		INT 21H
		
		;--SHOW 8
		MOV AH,02H
		MOV DL,NUM1
		ADD DL,NUM2
		ADD DL,30H
		INT 21H
		
		;----STOP PROGRAM
		MOV AX,4C00H
		INT 21H
MAIN ENDP
END MAIN