.MODEL SMALL
.STACK 100
.DATA
	EMSG DB "EVEN$"
	OMSG DB "ODD$"
	MSG DB "ENTER A DIGIT:$"
	NL DB 10,13,"$"
	INUM DB ?
	TWO DB 2
	REMAINDER DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;--OUTPUT MSG
	MOV AH,09H
	LEA DX,MSG
	INT 21H
	
	;--INPUT
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INUM,AL
	
	;--CALCULATION TO FIND ODD OR EVEN
	MOV AH,0
	MOV AL,INUM
	DIV TWO
	MOV REMAINDER,AH
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--COMPARE
	CMP REMAINDER,0
	JE PEVEN   
	MOV AH,09H 
	LEA DX,OMSG
	INT 21H
	JMP EXIT
	
PEVEN:
	MOV AH,09H
	LEA DX,EMSG
	INT 21H
	JMP EXIT
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN