.MODEL SMALL
.STACK 100
.DATA
	CHAR DB "A"
	STR1 DB "ENTER A DIGIT: $"
	INUM DB ?
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
;--DISPLAY MSG
	MOV AH,09H
	LEA DX,STR1
	INT 21H

;--INPUT NUMBER
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INUM,AL
	
;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
;--LOOP CHAR
	MOV CL,INUM
	MOV CH,0
DISPLAY:
	MOV AH,02H
	MOV DL,CHAR
	INT 21H
	INC CHAR
	LOOP DISPLAY
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN
	