.MODEL SMALL
.STACK 100
.DATA
	ZERO DB 0
	ONE DB 1
	MSG DB "ENTER A BIT:$"
	NL DB 10,13,"$"
	INUM DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;--OUTPUT MSG
	MOV AH,09H
	LEA DX,MSG
	INT 21H
	
	;--INPUT
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INUM,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--COMPARE
	CMP INUM,1
	JE PZERO   
	MOV AH,02H 
	MOV DL,ONE
	ADD DL,30H
	INT 21H
	JMP EXIT
	
PZERO:
	MOV AH,02H
	MOV DL,ZERO
	ADD DL,30H
	INT 21H
	JMP EXIT

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN