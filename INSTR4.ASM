.MODEL SMALL
.STACK 100
.DATA
	SECMSG DB "ENTER SECRET WORD: $"
	ENCRYPTMSG DB "ENTER ENCRYTION KEY(0-9): $"
	RESULTMSG DB "THE ENCRYPTED WORD IS $"
	NL DB 10,13,"$"
	
	SECRETCODE LABEL BYTE
	MAXN DB 13
	ACTN DB ?
	ISECRETCODE DB 13 DUP("$")
	ENCRYPTKEY DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,SECMSG
	INT 21H
	
	MOV AH,0AH
	LEA DX,SECRETCODE
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,ENCRYPTMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV ENCRYPTKEY,AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV CH,0
	MOV CL,ACTN
	MOV SI,0
	MOV BH,ENCRYPTKEY
	
ENCRYPT:
	ADD ISECRETCODE[SI],BH
	INC SI
	LOOP ENCRYPT
	
	MOV AH,09H
	LEA DX,RESULTMSG
	INT 21H
	
	MOV AH,09H
	LEA DX,ISECRETCODE
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN