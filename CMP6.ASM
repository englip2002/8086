.MODEL SMALL
.STACK 100
.DATA
	IDAY DB ?
	IMSG DB "ENTER A DAY: $"
	WDMSG DB "THIS IS A WEEKDAY$"
	WEMSG DB "THIS IS A WEEKEND$"
	OMSG DB "NOT A VALID DAY$"

	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV IDAY, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;CMP >7 OR <1
	CMP IDAY,7
	JG InvalidDay
	CMP IDAY,1
	JB InvalidDay
	
	;CMP >5
	CMP IDAY,5
	JG Weekend
	
	;>=1
	MOV AH,09H
	LEA DX,WDMSG
	INT 21H
	
	JMP EXIT
	
InvalidDay:
	MOV AH,09H
	LEA DX,OMSG
	INT 21H
	JMP EXIT

Weekend:
	MOV AH,09H
	LEA DX,WEMSG
	INT 21H
	JMP EXIT
		
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN