.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "ENTER A DIGIT: $"
	NL DB 13,10,"$"
	NUM DB 1
	INPUT DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;OUTPUT STR1
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;INPUT INPUT
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INPUT,AL
	
	;OUTPUT NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;LOOP
	MOV CL,INPUT
	MOV CH,0H
	L1:
		MOV AH,02H
		MOV DL,NUM
		ADD DL,30H
		INT 21H
		INC NUM
	LOOP L1
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN