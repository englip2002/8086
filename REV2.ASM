.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB 9
    NUM2 DB 5
    NUM3 DB 7
	LARGESTMSG DB "THE LARGEST IS $"
	NL DB 10,13,"$"

.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

    ;--CMP NUM1 AND NUM2
	MOV DL,NUM1
    CMP DL,NUM2
    JG CMP_NUM1_NUM3

    ;--CMP NUM2 AND NUM 3
    MOV DL,NUM2
    CMP DL,NUM3
    JG DISPLAY_LARGEST ;--DL=NUM2
    MOV DL,NUM3
    JMP DISPLAY_LARGEST ;--DL=NUM3

CMP_NUM1_NUM3:
    CMP DL,NUM3
    JG DISPLAY_LARGEST ;--DL=NUM1
    MOV DL,NUM3
    JMP DISPLAY_LARGEST ;--DL=NUM3

DISPLAY_LARGEST:
    MOV AH,02H
    ADD DL,30H
    INT 21H

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN