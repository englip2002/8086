.MODEL SMALL
.STACK 100
.DATA
	NUM DB 2,5,6,8,1,7
	TOTAL DB ?
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;DIRECT-OFFSER ADDRESSING
	MOV SI,0
	MOV CX,6
	
	CALCULATION:
		MOV DL,NUM[SI]
		ADD TOTAL,DL
		INC SI
	LOOP CALCULATION
	
	;--DISPLAY 2 NUM
	MOV AH,0
	MOV AL,TOTAL
	DIV TEN
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN