.MODEL SMALL
.STACK 100
.DATA
	GRADE DB ?
	IMSG DB "ENTER GRADE: $"
	NL DB 10,13,"$"
	AMSG DB "EXCELLENT$"
	CMSG DB "GOOD$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV GRADE,AL
	CMP GRADE,'A'
	JE GradeA
	CMP GRADE,'B'
	JE GradeA
	
	MOV AH,09H
	LEA DX,CMSG
	INT 21H
	JMP EXIT

GradeA:
	MOV AH,09H
	LEA DX,AMSG
	INT 21H
	

	EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN