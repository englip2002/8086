.MODEL SMALL
.STACK 100
.DATA
		CHAR1 DB "A"  ;--CHAR1 DB 'A'
		CHAR2 DB ","

.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;--SHOW CHAR A
		MOV AH,02H
		MOV DL,CHAR1
		INT 21H
		
		;--SHOW ,
		MOV AH,02H
		MOV DL,CHAR2
		INT 21H
		
		;--SHOW a
		MOV AH,02H
		MOV DL,CHAR1
		ADD DL,32
		INT 21H
		
		;--SHOW ,
		MOV AH,02H
		MOV DL,CHAR2
		INT 21H
		
		ADD CHAR1,2
		;--SHOW CHAR C
		MOV AH,02H
		MOV DL,CHAR1
		INT 21H
		
		;--SHOW ,
		MOV AH,02H
		MOV DL,CHAR2
		INT 21H
		
		;--SHOW c
		MOV AH,02H
		MOV DL,CHAR1
		ADD DL,32
		INT 21H
		
		;--SHOW ,
		MOV AH,02H
		MOV DL,CHAR2
		INT 21H
		
		ADD CHAR1,2
		;--SHOW CHAR E
		MOV AH,02H
		MOV DL,CHAR1
		INT 21H
		
		;--SHOW ,
		MOV AH,02H
		MOV DL,CHAR2
		INT 21H
		
		;--SHOW e
		MOV AH,02H
		MOV DL,CHAR1
		ADD DL,32
		INT 21H
		
		;--STOP PROGRAM
		MOV AX,4c00H
		INT 21H
MAIN ENDP
END MAIN