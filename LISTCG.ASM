.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB 4,7,2,9
	NUM2 DB 10,8,5,3
	TOTAL DB 4 DUP(0)
	TEN DB 10
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;DIRECT-OFFSER ADDRESSING
	MOV SI,0
	MOV CX,4
	
	CALCULATION:
		MOV DL,NUM1[SI]
		ADD DL,NUM2[SI]
		MOV TOTAL[SI],DL
		
		;--DISPLAY 2 NUM
		MOV AH,0
		MOV AL,TOTAL[SI]
		DIV TEN
		MOV BX,AX
	
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
	
		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H
		
		MOV AH,02H
		MOV DL,' '
		INT 21H
		
		INC SI
		
	LOOP CALCULATION

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN