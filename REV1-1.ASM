.MODEL SMALL
.STACK 100
.DATA
	LIST DB 12,29,-9,5,-48,20,7,-2,0
	TEN DB 10
	POSMSG DB "Positive: $"
	NEGMSG DB "Negative: $"
	NL DB 10,13,"$"
	NUMOFPOSITIVE DB 0
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

MOV SI,0
CMPLOOP:
	CMP LIST[SI],1
	JL CONLOOP
	INC NUMOFPOSITIVE

CONLOOP:
	INC SI
	CMP LIST[SI],0
	JNE CMPLOOP
	
DISPLAY:
	MOV AH,09H
	LEA DX,POSMSG
	INT 21H

	MOV AH,02H
	MOV DL,NUMOFPOSITIVE
	ADD DL,30H
	INT 21H

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV AH,09H
	LEA DX,NEGMSG
	INT 21H

	MOV AH,02H
	MOV DL,8
	SUB DL,NUMOFPOSITIVE
	ADD DL,30H
	INT 21H

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN