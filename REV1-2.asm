.MODEL SMALL
.STACK 100
.DATA
	NUM DB 1,2,3,4,5,6,7,8,9
	INPUTMSG DB "ENTER A DIGIT: $"
	NL DB 10,13,"$"
	INPUTNUM DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

	MOV AH,09H
	LEA DX,INPUTMSG
	INT 21H

	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INPUTNUM,AL

	MOV AH,09H
	LEA DX,NL
	INT 21H

	MOV CX,9
	MOV SI,0
CMPLOOP:
	MOV AH,0
	MOV AL,NUM[SI]
	DIV INPUTNUM
	CMP AH,0
	JE DISPLAYASTERIK

	MOV AH,02H
	MOV DL,NUM[SI]
	ADD DL,30H
	INT 21H
	JMP CONLOOP

DISPLAYASTERIK:
	MOV AH,02H
	MOV DL,"*"
	INT 21H

CONLOOP:
	INC SI
	LOOP CMPLOOP
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN