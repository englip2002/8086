.MODEL SMALL
.STACK 100
.DATA
	STR LABEL BYTE
	MAXN DB 10
	ACTN DB ?
	INPUTSTR DB 10 DUP("$")
	
	STR1 DB "ENTER A FRUIT: $"
	STR2 DB "YOU HAVE ENTERED $"
	NL DB 13,10,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;--INPUT STR
	MOV AH,0AH
	LEA DX,STR
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--USE LEA TO DISPLAY
	MOV AH,09H
	LEA DX,INPUTSTR
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN