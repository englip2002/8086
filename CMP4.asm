.MODEL SMALL
.STACK 100
.DATA
	ICODE DB ?
	IMSG DB "ENTER A CODE: $"
	PMSG DB "PINK$"
	RMSG DB "RED$"
	YMSG DB "YELLOW$"
	WMSG DB "WHITE$"
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV ICODE, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	CMP ICODE,'P'
	JE PINK
	
	CMP ICODE,'R'
	JE RED
	
	CMP ICODE,'Y'
	JE YELLOW
	
	MOV AH,09H
	LEA DX,WMSG
	INT 21H
	JMP EXIT
	
PINK:
	MOV AH,09H
	LEA DX,PMSG
	INT 21H
	JMP EXIT
	
RED:
	MOV AH,09H
	LEA DX,RMSG
	INT 21H
	JMP EXIT
	
YELLOW:
	MOV AH,09H
	LEA DX,YMSG
	INT 21H
	JMP EXIT
	

	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN