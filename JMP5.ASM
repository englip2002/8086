.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "ENTER VEHICLE CODE(C/M): $"
	STR2 DB "ENTER HOURS: $"
	STR3 DB "PARKING CHARGE= $"
	CRATE DB 2
	MRATE DB 1
	ICODE DB ?
	IHOUR DB ?
	CHARGE DB ?
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;--PRINT STR1
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;--INPUT VCODE
	MOV AH,01H
	INT 21H
	MOV ICODE, AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--PRINT STR2
	MOV AH,09H
	LEA DX,STR2
	INT 21H
	
	;--INPUT IHOUR
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV IHOUR, AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--PRINT STR3
	MOV AH,09H
	LEA DX,STR3
	INT 21H
	
	;--COMPARE   
	MOV AL,ICODE
	CMP AL,"C"
	JE CALCCHARGE
	
	MOV AH,0
	MOV AL,MRATE
	MUL IHOUR
	MOV CHARGE,AL

PRINTCHARGE:
	MOV AH,02H
	MOV DL,CHARGE
	ADD DL,30H
	INT 21H
	JMP EXIT
	
CALCCHARGE:
	MOV AH,0
	MOV AL,CRATE
	MUL IHOUR
	MOV CHARGE,AL
	JMP PRINTCHARGE
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN