.MODEL SMALL
.STACK 100
.DATA
		NUM1 DB 5
		NUM2 DB 3
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;5/3 ->AX/NUM2
		MOV AH,0
		MOV AL,NUM1 ;AX=5
		DIV NUM2    ;AH=02(Remainder),AL=01(Quotient)
		MOV BX,AX 
		
		;display quotient
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		;display remainder
		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H
		
		MOV AX,4c00H
		INT 21H
MAIN ENDP
END MAIN