.MODEL SMALL
.STACK 100
.DATA
		NUM DB 5
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;--DISPLAY A DIGIT 4
		MOV AH,02H
		MOV DL,NUM
		SUB DL,1     ;--DEC DL
		ADD DL,30H   ;ADD DL,48
		INT 21H
		
		;--DISPLAY A DIGIT 5
		MOV AH,02H
		MOV DL,NUM
		ADD DL,30H   ;ADD DL,48
		INT 21H
		
		;--DISPLAY A DIGIT 6
		MOV AH,02H
		MOV DL,NUM
		ADD DL,1
		ADD DL,30H   ;ADD DL,48
		INT 21H
		
		;--STOP PROGRAM
		MOV AX,4c00H
		INT 21H
		
MAIN ENDP
END MAIN