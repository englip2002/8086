.MODEL SMALL
.STACK 100
.DATA
	NUM1 DB 4,7,2,9
	NUM2 DB 10,8,5,3
	TEN DB 10
	TOTAL DB 4 DUP(0)
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CX,4
	MOV SI,0
	
	L1:
		MOV DL,NUM1[SI]
		ADD DL,NUM2[SI]
		MOV TOTAL[SI],DL
		
		MOV AH,0
		MOV AL,TOTAL[SI]
		DIV TEN
		MOV BX,AX
		
		MOV AH,02H
		MOV DL,BL
		ADD DL,30H
		INT 21H
		
		MOV AH,02H
		MOV DL,BH
		ADD DL,30H
		INT 21H
		
		MOV AH,02H
		MOV DL," "
		INT 21H
		
		INC SI
	LOOP L1
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN