.MODEL SMALL
.STACK 100
.DATA
	IDAY DB ?
	IMSG DB "ENTER A DAY: $"
	VMSG DB "VALID DAY$"
	IVMSG DB "NOT A VALID DAY$"
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV IDAY, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	CMP IDAY,1
	JL InvalidDay
	
	CMP IDAY,7
	JG InvalidDay
	
	MOV AH,09H
	LEA DX,VMSG
	INT 21H
	JMP EXIT
	
InvalidDay:
	MOV AH,09H
	LEA DX,IVMSG
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN