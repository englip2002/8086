.MODEL SMALL
.STACK 100
.DATA
	ALPHABETH DB 'A'
	CONMSG DB "CONTINUE PRINTING (Y/N): $"
	ICON DB ? 
	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

PRINTING:
	MOV AH,02H
	MOV DL,ALPHABETH
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,CONMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV ICON,AL
	
	;increase alphabeth by 1
	INC ALPHABETH
	
	;--next line
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;if is [ means alrd print Z
	CMP ALPHABETH,'['
	JE EXIT
	
	CMP ICON,'Y'
	JE PRINTING
	

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN