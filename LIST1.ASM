.MODEL SMALL
.STACK 100
.DATA
	LIST DB 7,4,9,6
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;DIRECT-OFFSER ADDRESSING
	MOV SI,0
	MOV CX,4
	
	L1:
		MOV AH,02H
		MOV DL,LIST[SI]
		ADD DL,30H
		INT 21H
		INC SI
	LOOP L1
	
	;INDIRECT-OFFSER ADDRESSING
	MOV SI,OFFSET LIST ;LEA SI,LIST
	MOV CX,4
	
	L2:
		MOV AH,02H
		MOV DL,[SI]
		ADD DL,30H
		INT 21H
		INC SI
	LOOP L2
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN