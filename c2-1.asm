.MODEL SMALL
.STACK 100
.DATA
	NUM DB 1
	NL DB 10,13,"$"
	THREE DB 3
	TEN DB 10
	RESULT DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
;--LOOP AND DISPLAY NUM*3=RESULT(2 DIGIT)
	MOV CL,9
	MOV CH,0
L1:
	MOV AH,02H
	MOV DL,NUM
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,'x'
	INT 21H
	
	MOV AH,02H
	MOV DL,3
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,"="
	INT 21H
	
	;--CALCULATION
	MOV AL,NUM
	MUL THREE
	MOV RESULT,AL
	
	;--DISPLAY 2 DIGIT
	MOV AH,0
	MOV AL,RESULT
	DIV TEN
	MOV BX, AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	INC NUM
	LOOP L1
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN