.MODEL SMALL
.STACK 100
.DATA
	STRM DB "MALE$"
	STRF DB "FEMALE$"
	MSG DB "ENTER GENDER(M/F):$"
	NL DB 10,13,"$"
	ICHAR DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;if (digit>=5)
	;	printf("pass")
	;else
	;	printf("fail")
	
	;--OUTPUT MSG
	MOV AH,09H
	LEA DX,MSG
	INT 21H
	
	;--INPUT
	MOV AH,01H
	INT 21H
	MOV ICHAR,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--COMPARE
	CMP ICHAR,'F'
	JE FEMALE   ;IF EQUAL JMP FEMALE
	MOV AH,09H  ;ELSE CONTINUE
	LEA DX,STRM
	INT 21H
	JMP EXIT
	
FEMALE:
	MOV AH,09H
	LEA DX,STRF
	INT 21H

EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN