.MODEL SMALL
.STACK 100
.DATA
	ICODE DB ?
	ICON DB ?
	IMSG DB "ENTER CODE(D/C): $"
	CONMSG DB "CONTINUE(Y/N): $"
	DMSG DB "DIPLOMA$"
	CMSG DB "CERTIFICATE$"
	BYE DB "BYE BYE$"

	NL DB 10,13,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX

EnterCode:
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV ICODE, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;CMP C
	CMP ICODE,'C'
	JE Certificate
	
	MOV AH,09H
	LEA DX,DMSG
	INT 21H
	
	JMP Continue
	
Certificate:
	MOV AH,09H
	LEA DX,CMSG
	INT 21H
	
Continue:
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,CONMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	MOV ICON, AL
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	CMP ICON,'N'
	JE EXIT
	JMP EnterCode
	

EXIT:
	MOV AH,09H
	LEA DX,BYE
	INT 21H
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN