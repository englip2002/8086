.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "ENTER AN OPERATOR(+,-): $"
	STR2 DB "ENTER 1ST NUMBER: $"
	STR3 DB "ENTER 2ST NUMBER: $"
	NL DB 10,13,"$"
	TEN DB 10
	IOPERATOR DB ?
	INUM1 DB ?
	INUM2 DB ?
	TOTAL DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;--PRINT STR1
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;--INPUT IOPERATOR
	MOV AH,01H
	INT 21H
	MOV IOPERATOR,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--PRINT STR2
	MOV AH,09H
	LEA DX,STR2
	INT 21H
	
	;--INPUT INUM1
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INUM1,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--PRINT STR3
	MOV AH,09H
	LEA DX,STR3
	INT 21H
	
	;--INPUT INUM2
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV INUM2,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;--PRINT 1+2=
	MOV AH,02H
	MOV DL,INUM1
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,IOPERATOR
	INT 21H
	
	MOV AH,02H
	MOV DL,INUM2
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,"="
	INT 21H
	
	;--COMPARE
	CMP IOPERATOR,"+"
	JE CALPLUS
	
	;--CAL SUBSTRACT
	MOV BL,INUM1
	SUB BL,INUM2
	MOV TOTAL,BL
	
DISPLAY:
	;--DISPLAY 2 DIGIT
	MOV AH,0
	MOV AL,TOTAL
	DIV TEN
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H
	
	JMP EXIT
	
CALPLUS:
	MOV BL,INUM1
	ADD BL,INUM2
	MOV TOTAL,BL
	JMP DISPLAY
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN