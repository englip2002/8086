.MODEL SMALL
.STACK 100
.DATA
	DATAMSG DB "PLEASE ENTER ANY DATA >> $"
	RESULTMSG DB "THE NUMERICAL VALUE IS >> $"
	NL DB 10,13,"$"
	
	DATA LABEL BYTE
	MAXN DB 13
	ACTN DB ?
	IDATA DB 13 DUP("$")
	
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,DATAMSG
	INT 21H
	
	MOV AH,0AH
	LEA DX,DATA
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX,RESULTMSG
	INT 21H
	
	MOV CH,0
	MOV CL,ACTN
	MOV SI,0
	
VALIDATE:
	SUB IDATA[SI],30H
	CMP IDATA[SI],0
	JB CONLOOP
	
	CMP IDATA[SI],9
	JA CONLOOP
	
	MOV AH,02H
	MOV DL,IDATA[SI]
	ADD DL,30H
	INT 21H
	
CONLOOP:
	INC SI
	LOOP VALIDATE
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN