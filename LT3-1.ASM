.MODEL SMALL
.STACK 100
.DATA
	NUM DB 5,7,2,9,6,5,8
	IMSG DB "ENTER INDEX(0-6): $"
	NL DB 10,13,"$"
	IDIGIT DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,IMSG
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV IDIGIT,AL
	
	;--NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,0
	MOV AL,IDIGIT
	MOV SI,AX
	
	MOV AH,02H
	MOV DL,NUM[SI]
	ADD DL,30H
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN