.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "ENTER A DIGIT: $"
	DIGIT DB ?
	NL DB 10,13,"$"
	TOTAL DB 0
	TEN DB 10
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CL,4
	MOV CH,0
L1:
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	MOV AH,01H
	INT 21H
	SUB AL,30H
	;--CALCULATION
	ADD TOTAL,AL
	MOV DIGIT,AL
	
	;--NL
	MOV AH,09H
	LEA DX, NL
	INT 21H
	
	LOOP L1
	
;--DISPLAY 2 DIGIT
	MOV AH,0
	MOV AL,TOTAL
	DIV TEN
	MOV BX,AX
	
	MOV AH,02H
	MOV DL,BL
	ADD DL,30H
	INT 21H
	
	MOV AH,02H
	MOV DL,BH
	ADD DL,30H
	INT 21H
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN