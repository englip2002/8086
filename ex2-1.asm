.MODEL SMALL
.STACK 100
.DATA
		NUM1 DB 7
		NUM2 DB 2
.CODE
MAIN PROC
		MOV AX,@DATA
		MOV DS,AX
		
		;--SAVE INTO REGISTER
		MOV AH,02H
		MOV DL,NUM1
		MOV BH,NUM2
		
		;--CALCULATION
		ADD DL,BH
		
		;--CONVERT TO ASCII
		ADD DL,30H
		INT 21H
		
		
		;--STOP PROGRAM
		MOV AX,4c00H
		INT 21H
		
MAIN ENDP
END MAIN