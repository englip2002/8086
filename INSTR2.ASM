.MODEL SMALL
.STACK 100
.DATA
	STR LABEL BYTE
	MAXN DB 10
	ACTN DB ?
	INPUTSTR DB 10 DUP("$")
	
	STR1 DB "ENTER A WORD: $"
	STR2 DB "THE THIRD CHARACTER IS $"
	NL DB 13,10,"$"
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;--INPUT STR
	MOV AH,0AH
	LEA DX,STR
	INT 21H
	
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	MOV AH,09H
	LEA DX, STR2
	INT 21H
	
	MOV AH,02H
	MOV DL,INPUTSTR[2]
	INT 21H
	
EXIT:
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN