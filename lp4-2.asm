.MODEL SMALL
.STACK 100
.DATA
	STR1 DB "ENTER 1ST DIGIT: $"
	STR2 DB "ENTER 2ST DIGIT: $"
	NL DB 13,10,"$"
	NUM1 DB ?
	NUM2 DB ?
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	;OUTPUT STR1
	MOV AH,09H
	LEA DX,STR1
	INT 21H
	
	;INPUT NUM1
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM1,AL
	
	;OUTPUT NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;OUTPUT STR2
	MOV AH,09H
	LEA DX,STR2
	INT 21H
	
	;INPUT NUM2
	MOV AH,01H
	INT 21H
	SUB AL,30H
	MOV NUM2,AL
	
	;OUTPUT NL
	MOV AH,09H
	LEA DX,NL
	INT 21H
	
	;LOOP
	MOV CL,NUM2
	SUB CL,NUM1
	ADD CL,1
	
	MOV CH,0H
	L1:
		MOV AH,02H
		MOV DL,NUM1
		ADD DL,30H
		INT 21H
		INC NUM1
	LOOP L1
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
END MAIN